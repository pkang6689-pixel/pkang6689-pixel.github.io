def build_flashcard_js(cards):
    """Build the JS array string from a list of flashcard dicts."""
    lines = []
    for c in cards:
        q = c["question"].replace("\\", "\\\\").replace('"', '\\"')
        a = c["answer"].replace("\\", "\\\\").replace('"', '\\"')
        lines.append(f'          {{ question: "{q}", answer: "{a}" }}')
    return "    window.lessonFlashcards = [\n" + ",\n".join(lines) + "\n      ];"


# ── Main ──────────────────────────────────────────────────────────────

base_dir = os.path.join(os.path.dirname(os.path.dirname(__file__)),
                        "ArisEdu Project Folder", "PhysicsLessons")

practice_files = glob.glob(os.path.join(base_dir, "Unit*", "Lesson*_Practice.html"))
print(f"Found {len(practice_files)} Practice files\n")

# Pattern to match the existing lessonFlashcards block
pattern = re.compile(
    r'(    window\.lessonFlashcards = \[).*?(\];)',
    re.DOTALL
)

modified = 0
skipped = 0

for filepath in sorted(practice_files):
    filename = os.path.basename(filepath)
    m = re.match(r'Lesson(\d+\.\d+)_Practice\.html', filename)
    if not m:
        continue
    lesson_num = m.group(1)

    if lesson_num not in FLASHCARDS:
        print(f"  SKIP (no content): {filename}")
        skipped += 1
        continue

    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()

    original = content
    new_js = build_flashcard_js(FLASHCARDS[lesson_num])
    new_content = pattern.sub(new_js, content)

    if new_content != original:
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(new_content)
        modified += 1
        print(f"  OK: {filename} ({len(FLASHCARDS[lesson_num])} cards)")
    else:
        print(f"  UNCHANGED: {filename}")

print(f"\nModified: {modified}/{len(practice_files)} files")
if skipped:
    print(f"Skipped: {skipped} (no content defined)")
