def mq(num, text, opts, ci, expl):
    return {"question_number": num, "question_text": text, "attempted": 2, "data_i18n": None,
            "options": [{"text": o, "is_correct": i == ci, "data_i18n": None} for i, o in enumerate(opts)],
            "explanation": expl}

def write_quiz(unit, ln, title, qs):
    path = os.path.join(BASE, f"Unit{unit}", f"Lesson{ln}_Quiz.json")
    data = {"unit": unit, "lesson_number": ln, "title": title, "course": "AP Physics C - Mechanics", "quiz_questions": qs}
    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, indent=2, ensure_ascii=False)
    print(f"Wrote {path} ({len(qs)} questions)")

