def build_translation_block():
    """Build the JS translation entries string."""
    all_translations = {}
    all_translations.update(TITLE_TRANSLATIONS)
    for d in [UNIT1_Q, UNIT2_Q, UNIT3_Q, UNIT4_Q, UNIT5_Q,
              UNIT6_Q, UNIT7_Q, UNIT8_Q, UNIT9_Q, UNIT10_Q, UNIT11_Q]:
        all_translations.update(d)

    lines = []
    lines.append("")
    lines.append("  /* ═══════ Physics Unit Test Translations ═══════ */")

    for en, zh in all_translations.items():
        escaped_en = en.replace('\\', '\\\\').replace('"', '\\"')
        escaped_zh = zh.replace('\\', '\\\\').replace('"', '\\"')
        lines.append(f'  "{escaped_en}": "{escaped_zh}",')

    return "\n".join(lines)


def main():
    with open(TRANSLATIONS_PATH, 'r', encoding='utf-8') as f:
        content = f.read()

    # Check if already injected
    if "Physics Unit Test Translations" in content:
        print("Physics Unit Test translations already present — skipping.")
        return

    block = build_translation_block()

    # Insert before the closing }; of the translations object
    # Find the last occurrence of "};" preceded by the navigation translations
    marker = '"Next Up: Practice": "下一步: 练习",'
    if marker not in content:
        print("ERROR: Could not find insertion marker in global_translations.js")
        return

    content = content.replace(
        marker,
        marker + "\n" + block
    )

    with open(TRANSLATIONS_PATH, 'w', encoding='utf-8') as f:
        f.write(content)

    # Count entries
    count = block.count('": "') 
    print(f"Injected {count} physics unit test translations into global_translations.js")


if __name__ == '__main__':
    main()
